    required this.child,
    this.scale = 1.05,
  });

  @override
  State<ScaleOnHover> createState() => _ScaleOnHoverState();
}

class _ScaleOnHoverState extends State<ScaleOnHover>
    with SingleTickerProviderStateMixin {
